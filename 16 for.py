for i in range(5, 12):
    print('Hello, world!', i)

for i in range(0, 10, 2):
    print('Hello, world!', i)

for i in range(5, 0, -1):
    print('Hello world!', i)

for i in reversed(range(5)):
    print('Hello, world!', i)

fruits = ('apple', 'ornage', 'grape')
for fruit in fruits:
    print(fruit)

for letter in 'Python':
    print(letter, end=' ')

for letter in reversed('Python'):
    print(letter, end=' ')